    public void updateAll(){
    String[] ids=Customer.getIds();
    cboId.removeAllItems();
    cboFromCustomer.removeAllItems();
    cboToCustomer.removeAllItems();
    cboUsername.removeAllItems();
    String[] accounts=DB.getColumn("account","accountnumber");
    for(String s:accounts){
        if(s!=null){
            cboFromCustomer.addItem(s);
            cboToCustomer.addItem(s);
        }
    }
    String[] users=DB.getColumn("customer","username");
    for(String u:users){
        if(u!=null){
            cboUsername.addItem(u);
        }
    }
    String[] fn=DB.getColumn("customer", "firstname");
    String[] ln=DB.getColumn("customer","lastname");
    //String[] names=new String[fn.length];
    cboCustomer.removeAllItems();
    for(int i=0; i<fn.length;i++){
        //names[i]=fn[i]+ln[i];
        if(fn[i]!=null){
            cboCustomer.addItem(fn[i]+" "+ln[i]);
        }
    }
    for(String i:ids){
        if(i!=null)
            cboId.addItem(i);
    }
    txtLog.setText(Logger.getLog());
    }
