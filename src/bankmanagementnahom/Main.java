    private void btnImageSelectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImageSelectActionPerformed
        JFileChooser chooser=new JFileChooser();
      int result=chooser.showOpenDialog(this);
      File f=chooser.getSelectedFile();
      if(result==JFileChooser.APPROVE_OPTION && f!=null){
          String path=f.getAbsolutePath();
          filePath=path;
          lblImageLabel.setIcon(new ImageIcon(path));
      }
    }//GEN-LAST:event_btnImageSelectActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String image=filePath;
        String username=(String)cboUsername.getSelectedItem();
        String password=User.findUser(username).getPassword();
        String pass1=String.valueOf(txtPassword.getPassword());
        String pass2=String.valueOf(txtRepeatPassword.getPassword());
        if(password.equalsIgnoreCase(pass1)){
           if(DB.update("user", "password", pass2,"username='"+username+"'")&&
            DB.update("user","imagepath",image,"username='"+username+"'")){
               
               Logger.addLog("Account update for: "+username);
               JOptionPane.showMessageDialog(this,"Customer data updated");
           }
           else JOptionPane.showMessageDialog(this,"Customer not updated!");
        }
        else JOptionPane.showMessageDialog(this,"Password is not correct!");
    }//GEN-LAST:event_jButton1ActionPerformed

